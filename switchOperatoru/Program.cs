using System;

namespace switchOperatoru
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 10;
            switch (x)
            {

                case 3:
                    Console.WriteLine("değeriniz 3");
                    break;
                case 13:
                    Console.WriteLine("değeriniz 13");
                    break;
                case 10:
                    Console.WriteLine("değeriniz 10");
                    break;
            }
        }
    }
}